    private void tblTestMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTestMouseClicked
        try {
            int row = tblTest.getSelectedRow();
            String strDepNo = tblTest.getValueAt(row, 0).toString();
            String strDepName = tblTest.getValueAt(row, 1).toString();
            String strDepLocation = tblTest.getValueAt(row, 2).toString();
            
            txtNo.setText(strDepNo);
            txtName.setText(strDepName);
            txtLocation.setText(strDepLocation); 
            
            btnAdd.setEnabled(false);
            btnDelete.setEnabled(true);
            btnubdate.setEnabled(true);
        } catch (Exception e) {
            Tools.msgBox(e.getMessage());
        }
    }//GEN-LAST:event_tblTestMouseClicked

