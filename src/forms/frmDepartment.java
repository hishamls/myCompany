    private void btnSerchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSerchActionPerformed
        String strSearch= "SELECT * FROM department WHERE ";
        if (rBNo.isSelected()) {
            strSearch += "depNo";
        } else if (rBName.isSelected()) {
            strSearch += "depName";
        } else {
            strSearch += "location";
        }
        strSearch += " LIKE '%" + txtSearch.getText()+ "%'";
        d.getCustomRows(strSearch, tblTest); 

    }//GEN-LAST:event_btnSerchActionPerformed

